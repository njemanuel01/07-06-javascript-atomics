age = prompt("How old are you?");
hometown = prompt("What is your hometown?");
fav_number = prompt("What is your favorite number?");

//Output values using alert
alert("Hi, " + name);
alert("You are " + age + " years old.");
alert("You were born in " + hometown);
alert("Your favorite number is " + fav_number);

